public class NumericTypes {
    public static void main(String[] args) {
      short  i = 0;
      long   l = 0;
      float  f = 0.0f;
      double d = 0.0d;
      System.out.format("i = %d%n", i);
      System.out.format("l = %d%n", l);
      System.out.format("f = %f%n", f);
      System.out.format("d = %f%n", d);
      System.out.format("d = %e%n", d);
    }
}
